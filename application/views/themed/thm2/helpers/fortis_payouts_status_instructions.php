<!-- Processing Status Tooltip Information -->
The payout process can return five distinct status IDs, each representing a specific stage in the settlement workflow.
<br>
<br>
Descriptions of each status:<br><br>
<ul style="margin-left: -10px!important;">
    <li><b>TO SETTLE:</b> Transaction is queued and ready for processing.</li>
    <li><b>SETTLED:</b> Transaction completed successfully. No further action needed.</li>
    <li><b>ERROR:</b> Issue during settlement. Contact administrator.</li>
    <li><b>RE-PROCESS:</b> Transaction needs re-processing due to failure in the previous attempt.</li>
    <li><b>PROCESSING:</b> Transaction is being processed. Wait for completion.</li>
    <li><b>PENDING ORIGINATION:</b> ACH transaction initiated. No funds moved yet.</li>
    <li><b>ORIGINATING:</b> ACH transaction initiated. No funds moved yet.</li>
    <li><b>ORIGINATED:</b> ACH Transaction in process, but not funded/settled yet.</li>
</ul>